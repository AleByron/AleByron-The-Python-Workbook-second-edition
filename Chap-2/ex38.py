x = int(input('Insert the number of sides of your shape:'))
if x==3:
    print('Your figure is a triangle')
elif x==4:
    print('Your figure is a quadrilateral')
elif x == 5:
    print('Your figure is a pentagon')
elif x == 6:
    print('Your figure is a hexagon')
elif x == 7:
    print('Your figure is a heptagon')
elif x == 8:
    print('Your figure is a octagon')
elif x == 9:
    print('Your figure is a nonagon')
elif x == 10:
    print('Your figure is a decagon')
else:
    print('You figure got too many sides')